function solution(arr) {
  for (let i = arr.length - 1; i > 0 - 1; i--) {
    for (let j = 0; j < i; j++) {
      if (arr[j] > arr[j + 1]) {
        [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]];
      }
    }
  }
  return arr;
}

console.log(solution([13, 5, 11, 7, 23, 15]));

